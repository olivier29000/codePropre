package ex1;

/**
 * Classe de méthodes de calculs (addition, soustraction)
 * 
 * @author Diginamic02
 *
 */
public class calcul {

	/**
	 * Methode réalise l'addition des paramètres a et b
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Methode réalise l'addition des paramètres a et b (a-b)
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
